// ------------------------------
// 1. 変数とは何か
// ------------------------------
// 「値（数字や文字）に名前をつけて、あとで使えるようにする箱」みたいなもの。
// JavaScript では主に 2 種類の宣言をよく使う：
//
//   let  : 中身をあとで変更してもよい変数
//   const: 一度決めたら中身を変えない（変えられない）変数
//
// まずは let から。

let message = "これは let で宣言した message だぜぇ";
// 上の行の意味：
//   let        -> 「変数を作るよ」というキーワード
//   message    -> 変数の名前（好きな名前で OK、ただしルールあり）
//   =          -> 右側の値を左側の変数に入れる（代入）
//   "..."      -> 文字列（ダブルクォートで囲まれた文字）
//   ;          -> 文の終わり（JavaScript では省略もできるが、今は付けておく）

console.log("最初の message:", message);
// console.log(...) は「中身をターミナルに出す」関数。
// カンマで区切ると、複数の値を連続して出せる。

// ------------------------------
// 2. 変数の中身を変更してみる（let）
// ------------------------------
message = "message の中身をあとから変えたぜぇ";
// let は「再代入OK」なので、上書きできる。

console.log("変更後の message:", message);

// ------------------------------
// 3. 数字の変数と簡単な計算
// ------------------------------

// 数字を入れた変数
let count = 1;
console.log("count の最初の値:", count);

// 足し算（1 + 1）
count = count + 1; // 今の count（1）に 1 を足して、また count に入れ直している
console.log("1回目の更新後 count:", count);

// 「今の値に 5 を足す」を短く書いた形が +=
count += 5; // 「count = count + 5;」と同じ意味
console.log("2回目の更新後 count:", count);

// ------------------------------
// 4. const: 中身を変えない前提の変数
// ------------------------------

// 例：名前は基本的に変わらない前提で const にしておく
const name = "Tokum";
console.log("name:", name);

// 下のように const の中身を変えようとするとエラーになる：
// 実験したいときはコメントを外して `node main.js` してみる。
// ※ 実行するとそこで止まるので、練習が終わったらまたコメントに戻しておくこと。
//
// name = "変えようとするとエラーになる"; // TypeError: Assignment to constant variable.

// ------------------------------
// 5. 複数の数で計算する（+ - * /）
// ------------------------------

// 2つの数を変数に入れる
let x = 10;
let y = 3;

console.log("x:", x);
console.log("y:", y);

// 基本の計算
let plus = x + y;    // 足し算
let minus = x - y;   // 引き算
let times = x * y;   // 掛け算
let divide = x / y;  // 割り算（JavaScript では小数も出る）

console.log("x + y =", plus);
console.log("x - y =", minus);
console.log("x * y =", times);
console.log("x / y =", divide);

// ------------------------------
// 6. 文字列（テキスト）と数値を組み合わせる
// ------------------------------

// ふつうの文字列の足し算（結合）
let hello = "こんにちは、";
let target = "世界";
let greeting = hello + target + "！"; // 「こんにちは、世界！」になる
console.log(greeting);

// 数字も一緒に文字列にしたいとき：
// 例）年齢
let age = 20;

// 文字列 + 数字 も一応動くが、少し分かりにくい書き方になることが多い。
let ageMessage1 = "今の年齢は " + age + " 歳です。";
console.log(ageMessage1);

// ------------------------------
// 7. テンプレートリテラル（おすすめの書き方）
// ------------------------------
// バッククォート（キーボードの左上にある ` のやつ）で囲んで、
// 文字列の中に ${変数} という形で変数を埋め込める。
// こっちの方が読みやすくてミスが少ないので、現代JSではかなりよく使う。

let ageMessage2 = `今の年齢は ${age} 歳です。`;
console.log(ageMessage2);

let summary = `名前は ${name}、年齢は ${age} 歳、count は今 ${count} です。`;
console.log(summary);

